#!/bin/bash

# Instalar Cyberduck (alternativa ao WinSCP)
echo "Instalando o Cyberduck (alternativa ao WinSCP)..."
brew install --cask cyberduck

# Instalar Anydesk 
echo "Instalando o Anydesk..."
brew install --cask anydesk

# Instalar Discord (alternativa ao Slack)
echo "Instalando o Discord..."
brew install --cask discord

# Instalar Spotify (alternativa de música)
echo "Instalando o Spotify..."
brew install --cask spotify

# Instalar Git (para desenvolvimento)
echo "Instalando o Git..."
brew install git

# Instalar Docker (para DevOps)
echo "Instalando o Docker..."
brew install --cask docker

# Instalar Terraform (para infraestrutura como código - DevOps)
echo "Instalando o Terraform..."
brew install terraform

# Instalar Nmap (ferramenta de rede, útil para análise de redes)
echo "Instalando o Nmap..."
brew install nmap

# Instalar Wireshark (ferramenta para captura e análise de pacotes de rede)
echo "Instalando o Wireshark..."
brew install --cask wireshark

# Instalar Trello (gestão de tarefas e projetos)
echo "Instalando o Trello..."
brew install --cask trello

# Finalizando
echo "Instalações concluídas com sucesso!"
